<script lang="ts" setup>
import type {
  OnActionClickParams,
  VxeTableGridOptions,
} from '#/adapter/vxe-table';
import type { SystemAppApi } from '#/api/system/app';

import { Page, useModal } from '@finn/common-ui';
import { Plus } from '@finn/icons';

import { Button, message } from 'ant-design-vue';

import { useVxeGrid } from '#/adapter/vxe-table';
import { deleteFunc, getFuncPage } from '#/api/system/app';
import { $t } from '#/locales';

import { useFuncListColumns, useGridFormSchema } from './data';
import FuncForm from './modules/funcForm.vue';

const [FormModal, formModalApi] = useModal({
  connectedComponent: FuncForm,
  destroyOnClose: true,
});

const [Grid, gridApi] = useVxeGrid({
  showSearchForm: false, // 默认隐藏搜索表单
  formOptions: {
    fieldMappingTime: [['createTime', ['startTime', 'endTime']]],
    schema: useGridFormSchema(),
    submitOnChange: true,
    showCollapseButton: false, // 是否显示展开/折叠
  },
  gridOptions: {
    columns: useFuncListColumns(onActionClick),
    height: 'auto',
    keepSource: true,
    proxyConfig: {
      ajax: {
        query: async ({ page }, formValues) => {
          return await getFuncPage({
            pageNum: page.currentPage,
            pageSize: page.pageSize,
            ...formValues,
          });
        },
      },
    },
    rowConfig: {
      keyField: 'id',
      isCurrent: true, // 高亮选中行
    },
    toolbarConfig: {
      custom: true,
      export: false,
      refresh: { code: 'query' },
      search: true,
      zoom: true,
    },
  } as VxeTableGridOptions<SystemAppApi.Func>,
});

function onActionClick(e: OnActionClickParams<SystemAppApi.Func>) {
  switch (e.code) {
    case 'delete': {
      onDelete(e.row);
      break;
    }
    case 'edit': {
      onEdit(e.row);
      break;
    }
  }
}

function onEdit(row: SystemAppApi.Func) {
  formModalApi.setData(row).open();
}

function onDelete(row: SystemAppApi.Func) {
  const hideLoading = message.loading({
    content: $t('ui.actionMessage.deleting', [row.name]),
    duration: 0,
    key: 'action_process_msg',
  });
  deleteFunc(row.id)
    .then(() => {
      message.success({
        content: $t('ui.actionMessage.deleteSuccess', [row.name]),
        key: 'action_process_msg',
      });
      onRefresh();
    })
    .catch(() => {
      hideLoading();
    });
}

function onRefresh() {
  gridApi.query();
}

function onCreate() {
  formModalApi.setData({}).open();
}
</script>
<template>
  <Page auto-content-height>
    <FormModal @success="onRefresh" />
    <Grid table-title="接口列表">
      <template #toolbar-tools>
        <Button type="primary" @click="onCreate">
          <Plus class="size-5" />
          新增接口
        </Button>
      </template>
    </Grid>
  </Page>
</template>
